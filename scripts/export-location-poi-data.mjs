#!/usr/bin/env node
/**
 * Yerelde hazırlanmış lokasyon/ilan POI JSON verisini üretime uygulanabilir SQL'e aktarır.
 *
 * Örnek:
 *   node scripts/export-location-poi-data.mjs --out tmp/location-poi-data.sql
 *   ./deploy/apply-sql.sh tmp/location-poi-data.sql
 */
import fs from 'node:fs'
import path from 'node:path'
import { createPgClient } from './lib/pg-client.mjs'

const args = process.argv.slice(2)
const outArg = args.find((a) => a.startsWith('--out='))?.slice('--out='.length) ?? 'tmp/location-poi-data.sql'
const includeListings = !args.includes('--no-listings')

function sqlLiteral(value) {
  if (value == null) return 'NULL'
  return `'${String(value).replace(/'/g, "''")}'`
}

function sqlJsonb(value) {
  const raw = value == null || String(value).trim() === '' ? '[]' : String(value)
  return `${sqlLiteral(raw)}::jsonb`
}

async function main() {
  const pg = createPgClient()
  await pg.connect()
  try {
    const { rows: pages } = await pg.query(
      `SELECT id::text, travel_ideas_json::text AS travel, service_pois_json::text AS service
       FROM location_pages
       WHERE coalesce(jsonb_array_length(travel_ideas_json), 0) > 0
          OR coalesce(jsonb_array_length(service_pois_json), 0) > 0
       ORDER BY slug_path`,
    )
    const listings = includeListings
      ? (await pg.query(
          `SELECT id::text, nearby_pois_json::text AS nearby
           FROM listings
           WHERE nearby_pois_json IS NOT NULL
             AND coalesce(jsonb_array_length(nearby_pois_json), 0) > 0
           ORDER BY id`,
        )).rows
      : []

    const lines = [
      '-- Generated by scripts/export-location-poi-data.mjs',
      '-- Applies prepared location_pages travel/service POIs and listing nearby POIs.',
      'BEGIN;',
      '',
    ]

    for (const row of pages) {
      lines.push(
        `UPDATE location_pages SET travel_ideas_json = ${sqlJsonb(row.travel)}, service_pois_json = ${sqlJsonb(row.service)} WHERE id = ${sqlLiteral(row.id)}::uuid;`,
      )
    }

    if (includeListings) {
      lines.push('')
      for (const row of listings) {
        lines.push(
          `UPDATE listings SET nearby_pois_json = ${sqlJsonb(row.nearby)} WHERE id = ${sqlLiteral(row.id)}::uuid;`,
        )
      }
    }

    lines.push('', 'COMMIT;', '')

    const outPath = path.resolve(outArg)
    fs.mkdirSync(path.dirname(outPath), { recursive: true })
    fs.writeFileSync(outPath, lines.join('\n'), 'utf8')
    console.log(`[OK] wrote ${outPath}`)
    console.log(`[OK] location_pages=${pages.length} listings=${listings.length}`)
  } finally {
    await pg.end()
  }
}

main().catch((err) => {
  console.error('[FAIL]', err?.message || err)
  process.exit(1)
})
