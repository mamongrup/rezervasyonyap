function categorySegments(
  locale: string,
  map: Record<string, string>,
): { locale: string; logical_key: string; path_segment: string }[] {
  return Object.entries(map).map(([logical_key, path_segment]) => ({
    locale,
    logical_key,
    path_segment,
  }))
}
