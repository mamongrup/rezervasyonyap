/**
 * İngilizce kaynak nesnesinin yapısını korur, yaprak stringleri `string` yapar — böylece
 * `de`, `tr` vb. çeviriler `"Katalog"` gibi farklı sabitlerle `AppMessages` ile uyumlu olur.
 */
type AppMessagesShape<T> = T extends string
  ? string
  : T extends readonly (infer U)[]
    ? readonly AppMessagesShape<U>[]
    : T extends object
      ? { [K in keyof T]: AppMessagesShape<T[K]> }
      : T

export type AppMessages = AppMessagesShape<typeof en>
